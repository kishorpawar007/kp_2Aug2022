package methods_lecture;

public class Use_of_switches_string {

	public static void main(String[] args) {
		
		switch("Pratik") {
		
		case "Rajesh":System.out.println("Welcome Rajesh");
		break;
		
		case "Mahesh":System.out.println("Welcome Mahesh");
		break;
		
		case "Kishor":System.out.println("Welcome Kishor");
		break;
		
		default: System.out.println("Wrong name entered");
		}
	}
}
